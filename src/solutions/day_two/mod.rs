#[derive(Clone, Copy)]
enum State {
  Unset,
  Undefined,
  Decreasing,
  Increasing,
}

const STATE_MAP: [State; 3] = [State::Decreasing, State::Undefined, State::Increasing];

pub fn is_safe(report: &[i8]) -> bool {
  let mut state: State = State::Unset;

  for (idx, entry) in report.iter().enumerate() {
    if idx > 0 {
      let diff = entry - report[idx - 1];
      match (state, diff.cmp(&0)) {
        (_, Ordering::Equal)
        | (State::Decreasing, Ordering::Greater)
        | (State::Increasing, Ordering::Less) => return false,
        (State::Unset, ord @ (Ordering::Less | Ordering::Greater)) => {
          state = STATE_MAP[((ord as i8) + 1) as usize]
        },
        _ => {},
      }

      let diff_abs = diff.abs();
      if diff_abs < 1 || diff_abs > 3 {
        return false;
      }
    }
  }

  true
}
